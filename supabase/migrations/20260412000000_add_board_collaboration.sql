-- ============================================================
-- Board Collaboration: tables, types, helper function, and RLS
-- ============================================================

-- 1. Custom enum for collaboration roles
CREATE TYPE public.collaborator_role AS ENUM ('owner', 'editor', 'viewer');

-- 2. Custom enum for invitation status
CREATE TYPE public.invitation_status AS ENUM ('pending', 'accepted', 'declined');

-- ============================================================
-- board_collaborators: bridging table linking boards to users
-- ============================================================
CREATE TABLE public.board_collaborators (
  id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  board_id    bigint NOT NULL REFERENCES public.boards(id) ON DELETE CASCADE,
  user_id     uuid   NOT NULL REFERENCES auth.users(id)    ON DELETE CASCADE,
  role        public.collaborator_role NOT NULL DEFAULT 'viewer',
  invited_by  uuid   REFERENCES auth.users(id) ON DELETE SET NULL,
  status      public.invitation_status NOT NULL DEFAULT 'pending',
  created_at  timestamptz NOT NULL DEFAULT now(),
  accepted_at timestamptz,
  UNIQUE (board_id, user_id)
);

CREATE INDEX idx_board_collaborators_board_id ON public.board_collaborators (board_id);
CREATE INDEX idx_board_collaborators_user_id  ON public.board_collaborators (user_id);

-- ============================================================
-- board_invitations: tracks invitations sent by email
-- (supports inviting users who may not have an account yet)
-- ============================================================
CREATE TABLE public.board_invitations (
  id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  board_id    bigint NOT NULL REFERENCES public.boards(id) ON DELETE CASCADE,
  email       character varying(255) NOT NULL,
  role        public.collaborator_role NOT NULL DEFAULT 'viewer',
  invited_by  uuid   NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  token       uuid   NOT NULL DEFAULT gen_random_uuid(),
  created_at  timestamptz NOT NULL DEFAULT now(),
  expires_at  timestamptz NOT NULL DEFAULT (now() + interval '7 days'),
  UNIQUE (board_id, email)
);

CREATE INDEX idx_board_invitations_board_id ON public.board_invitations (board_id);
CREATE INDEX idx_board_invitations_email    ON public.board_invitations (email);
CREATE INDEX idx_board_invitations_token    ON public.board_invitations (token);

-- ============================================================
-- Helper function: check if the current user is the board owner
-- or an accepted collaborator with at least the given role.
-- Role hierarchy: owner > editor > viewer
-- ============================================================
CREATE OR REPLACE FUNCTION public.has_board_access(
  p_board_id bigint,
  p_min_role public.collaborator_role DEFAULT 'viewer'
)
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
AS $$
  SELECT EXISTS (
    -- Board owner always has access
    SELECT 1 FROM public.boards b
    WHERE b.id = p_board_id AND b.user_id = auth.uid()
  )
  OR EXISTS (
    SELECT 1 FROM public.board_collaborators bc
    WHERE bc.board_id = p_board_id
      AND bc.user_id  = auth.uid()
      AND bc.status   = 'accepted'
      AND CASE p_min_role
            WHEN 'viewer' THEN bc.role IN ('viewer', 'editor', 'owner')
            WHEN 'editor' THEN bc.role IN ('editor', 'owner')
            WHEN 'owner'  THEN bc.role = 'owner'
          END
  );
$$;

-- ============================================================
-- RLS for board_collaborators
