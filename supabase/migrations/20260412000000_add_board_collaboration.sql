ALTER TABLE public.board_collaborators ENABLE ROW LEVEL SECURITY;

-- Board owners can see all collaborators on their boards
CREATE POLICY "Board owners can view collaborators"
  ON public.board_collaborators FOR SELECT
  USING (
    EXISTS (
      SELECT 1 FROM public.boards b
      WHERE b.id = board_id AND b.user_id = auth.uid()
    )
    OR user_id = auth.uid()
  );

-- Only board owners can invite collaborators
CREATE POLICY "Board owners can insert collaborators"
  ON public.board_collaborators FOR INSERT
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM public.boards b
      WHERE b.id = board_id AND b.user_id = auth.uid()
    )
  );

-- Board owners can update collaborator roles; collaborators can accept/decline
CREATE POLICY "Board owners and invitees can update collaborators"
  ON public.board_collaborators FOR UPDATE
  USING (
    EXISTS (
      SELECT 1 FROM public.boards b
      WHERE b.id = board_id AND b.user_id = auth.uid()
    )
    OR user_id = auth.uid()
  )
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM public.boards b
      WHERE b.id = board_id AND b.user_id = auth.uid()
    )
    OR user_id = auth.uid()
  );

-- Only board owners can remove collaborators
CREATE POLICY "Board owners can delete collaborators"
  ON public.board_collaborators FOR DELETE
  USING (
    EXISTS (
      SELECT 1 FROM public.boards b
      WHERE b.id = board_id AND b.user_id = auth.uid()
    )
    OR user_id = auth.uid()
  );

